fun main(){
    // Any型
    /*
     * Any型は全てのクラスの親クラス
     * Kotlinを記述していると、型の不明な変数を扱うことがあり、
     * Any型を付与することで特定の値の型をチェックを無効にし、コンパイルを通過させれる
     * ただし、AnyはKotlinの型安全の恩恵を受けれないので、できる限りAny型を使わない
     * 型安全なプロジェクトを目指すことが望ましい
     */

    var counter : Any = 1
    counter = "counter" // コンパイルエラーにならない
    counter = 1.567 // コンパイルエラーにならない
}